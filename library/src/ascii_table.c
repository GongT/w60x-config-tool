const char *dump_ascii(char chr)
{
	if (chr >= 0 && chr <= 127)
	{
		return ascii_table[(int)chr];
	}
	else
	{
		static char out_of_range[5];
		snprintf(out_of_range, 5, "0x%02X", chr & 0xFF);
		return out_of_range;
	}
}
